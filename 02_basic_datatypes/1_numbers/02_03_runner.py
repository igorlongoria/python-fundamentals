'''

If a runner runs 10 miles in 30 minutes and 30 seconds,
What is his/her average speed in kilometers per hour? (Tip: 1 mile = 1.6 km)

'''

run_distance_in_miles = 10
kilometers_in_1_mile = 1.6
run_distance_in_kilometers = run_distance_in_miles * kilometers_in_1_mile
run_time_in_minutes = 30.5
x = run_distance_in_kilometers
y = run_time_in_minutes
average_speed_per_hour = (x / y) * 60
z = average_speed_per_hour
a = "km/hr"
print(z, a)

